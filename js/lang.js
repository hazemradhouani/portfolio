(function() {
  'use strict';

  const langConfig = [
    { code: 'ar', label: 'العربية', dir: 'rtl' },
    { code: 'de', label: 'Deutsch', dir: 'ltr' },
    { code: 'en', label: 'English', dir: 'ltr' },
    { code: 'fr', label: 'Français', dir: 'ltr' },
    { code: 'ja', label: '日本語', dir: 'ltr' },
    { code: 'es', label: 'Español', dir: 'ltr' }
  ];

  let currentLang = localStorage.getItem('lang') || 'en';
  if (!langConfig.find(l => l.code === currentLang)) currentLang = 'en';

  window.applyLanguage = function(lang) {
    currentLang = lang;
    localStorage.setItem('lang', lang);
    const config = langConfig.find(l => l.code === lang);

    document.documentElement.lang = lang;
    document.documentElement.dir = config.dir;

    const dict = translations[lang];
    if (!dict) return;

    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (dict[key]) {
        if (el.tagName === 'META') {
          el.setAttribute('content', dict[key]);
        } else {
          el.textContent = dict[key];
        }
      }
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.getAttribute('data-i18n-placeholder');
      if (dict[key]) el.setAttribute('placeholder', dict[key]);
    });

    document.querySelectorAll('[data-i18n-title]').forEach(el => {
      const key = el.getAttribute('data-i18n-title');
      if (dict[key]) el.setAttribute('title', dict[key]);
    });

    document.querySelectorAll('[data-i18n-aria]').forEach(el => {
      const key = el.getAttribute('data-i18n-aria');
      if (dict[key]) el.setAttribute('aria-label', dict[key]);
    });

    const btnText = document.querySelector('.lang-selector__current');
    if (btnText) btnText.textContent = config.label;
  };

  // Immediate translate call
  applyLanguage(currentLang);

  function renderLanguageSelector() {
    // Only inject UI CSS if the wrapper isn't already populated
    if (document.querySelector('.lang-selector')) return;
    
    const style = document.createElement('style');
    style.textContent = `
      .lang-selector { position: relative; display: inline-block; font-family: var(--font-b); z-index: 999; }
      .lang-selector__btn { background: transparent; border: 1px solid var(--g200); color: var(--black); padding: 0.4rem 0.9rem; border-radius: 4px; cursor: pointer; display: inline-flex; align-items: center; gap: 0.6rem; font-size: 0.85rem; font-family: inherit; transition: border-color 0.3s var(--ease-gentle), background 0.3s var(--ease-gentle); }
      .lang-selector__btn:hover, .lang-selector__btn:focus { border-color: var(--g600); background: var(--g50); outline: none; }
      .lang-selector__icon { width: 14px; height: 14px; transition: transform 0.4s var(--ease-silk); }
      .lang-selector.is-open .lang-selector__icon { transform: rotate(180deg); }
      .lang-selector__dropdown { position: absolute; top: calc(100% + 8px); right: 0; background: var(--white); border: 1px solid var(--g100); border-radius: 6px; box-shadow: 0 4px 16px rgba(0,0,0,0.06); min-width: 130px; opacity: 0; visibility: hidden; transform: translateY(-8px); transition: opacity 0.3s var(--ease-gentle), transform 0.4s var(--ease-spring), visibility 0.3s; overflow: hidden; }
      html[dir="rtl"] .lang-selector__dropdown { right: auto; left: 0; }
      .lang-selector.is-open .lang-selector__dropdown { opacity: 1; visibility: visible; transform: translateY(0); }
      .lang-selector__option { display: block; width: 100%; text-align: left; padding: 0.6rem 1.2rem; background: transparent; border: none; color: var(--g600); cursor: pointer; transition: background 0.2s var(--ease-gentle), color 0.2s var(--ease-gentle); font-family: inherit; font-size: 0.85rem; }
      html[dir="rtl"] .lang-selector__option { text-align: right; }
      .lang-selector__option:hover, .lang-selector__option:focus { background: var(--g50); color: var(--black); outline: none; }
      .lang-selector__option[aria-selected="true"] { color: var(--black); font-weight: 500; background: var(--g80); }
    `;
    document.head.appendChild(style);

    const wrapper = document.createElement('div');
    wrapper.className = 'lang-selector';
    const currentConfig = langConfig.find(l => l.code === currentLang);
    const btn = document.createElement('button');
    btn.className = 'lang-selector__btn';
    btn.setAttribute('aria-haspopup', 'listbox');
    btn.setAttribute('aria-expanded', 'false');
    btn.setAttribute('aria-label', 'Select language');
    btn.innerHTML = `<span class="lang-selector__current">${currentConfig.label}</span><svg class="lang-selector__icon" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="1.5" stroke-linecap="round" stroke-linejoin="round"><polyline points="6 9 12 15 18 9"></polyline></svg>`;
    
    const dropdown = document.createElement('div');
    dropdown.className = 'lang-selector__dropdown';
    dropdown.setAttribute('role', 'listbox');

    langConfig.forEach(l => {
      const option = document.createElement('button');
      option.className = 'lang-selector__option';
      option.setAttribute('role', 'option');
      option.setAttribute('aria-selected', l.code === currentLang ? 'true' : 'false');
      option.setAttribute('data-lang', l.code);
      option.textContent = l.label;
      option.addEventListener('click', () => {
        applyLanguage(l.code);
        closeDropdown();
        dropdown.querySelectorAll('.lang-selector__option').forEach(opt => {
          opt.setAttribute('aria-selected', opt.getAttribute('data-lang') === l.code ? 'true' : 'false');
        });
      });
      dropdown.appendChild(option);
    });

    wrapper.appendChild(btn);
    wrapper.appendChild(dropdown);

    const navElement = document.getElementById('lang-switcher');
    if (navElement) {
        navElement.appendChild(wrapper);
    } else {
        const nav = document.querySelector('nav');
        if(nav) nav.prepend(wrapper);
    }

    let isOpen = false;
    function toggleDropdown() { isOpen = !isOpen; wrapper.classList.toggle('is-open', isOpen); btn.setAttribute('aria-expanded', isOpen.toString()); }
    function closeDropdown() { if (!isOpen) return; isOpen = false; wrapper.classList.remove('is-open'); btn.setAttribute('aria-expanded', 'false'); }
    btn.addEventListener('click', (e) => { e.stopPropagation(); toggleDropdown(); });
    document.addEventListener('click', (e) => { if (!wrapper.contains(e.target)) closeDropdown(); });
    wrapper.addEventListener('keydown', (e) => { if (e.key === 'Escape') { closeDropdown(); btn.focus(); } });
  }

  document.addEventListener('DOMContentLoaded', renderLanguageSelector);
})();
